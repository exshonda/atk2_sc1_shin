def make_param_info(s_file_name):
    if not os.path.isfile(s_file_name):
        sys.exit("Argument error !! [{0}]".format(s_file_name))

    s_param_file = os.path.join(
        os.path.dirname(s_file_name) or ".", "param_info.yaml")

    a_target_module = ["Rte", "Os", "Com", "PduR", "CanTp", "CanIf", "Can",
                       "EcuC", "EcuM", "WdgM", "WdgIf", "Wdg", "Dem"]

    root = _parse_xml(s_file_name)

    _State.foreign_ref_type = OrderedDict()
    _State.choice_container = []

    # NCES 仕様コンテナを予め設定
    h_result = yaml.safe_load(_NCES_CONTAINER)
    h_result_ord = OrderedDict()
    for k, v in h_result.items():
        h_result_ord[k] = list(v)
    _State.result = h_result_ord

    # /AUTOSAR/AR-PACKAGES/AR-PACKAGE/AR-PACKAGES/AR-PACKAGE/ELEMENTS/ECUC-MODULE-DEF
    for c_mod in root.findall(
            "AR-PACKAGES/AR-PACKAGE/AR-PACKAGES/AR-PACKAGE/ELEMENTS/"
            "ECUC-MODULE-DEF"):
        s_name = _find_text(c_mod, "SHORT-NAME")
        if s_name not in a_target_module:
            continue
        for c_cont in c_mod.findall(
                "CONTAINERS/ECUC-PARAM-CONF-CONTAINER-DEF"):
            _make_param_info_parse_parameter(c_cont)

    # パラメータ名でソートと重複除去
    h_sorted = OrderedDict()
    for k, v in _State.result.items():
        h_sorted[k] = sorted(set(v))

    # 外部参照先のデータ型格納辞書を結合
    if _State.foreign_ref_type:
        h_sorted["FOREIGN_REF_TYPE"] = OrderedDict(
            sorted(_State.foreign_ref_type.items()))

    # 選択型のコンテナ格納配列を追加
    if _State.choice_container:
        h_sorted["ECUC_CHOICE_CONTAINER_DEF"] = sorted(
            set(_State.choice_container))

    # RteSoftwareComponentInstanceRef は外部参照とインスタンス参照の
    # 両方に含まれるが，外部参照として扱う
    if "ECUC-INSTANCE-REFERENCE-DEF" in h_sorted:
        try:
            h_sorted["ECUC-INSTANCE-REFERENCE-DEF"].remove(
                "RteSoftwareComponentInstanceRef")
        except ValueError:
            pass

    _dump_yaml(h_sorted, s_param_file)
    print("Generated {0}".format(s_param_file))


def _make_param_info_parse_sub_container(c_elem):
    for c in list(c_elem):
        if c.tag == "ECUC-CHOICE-CONTAINER-DEF":
            _State.choice_container.append(_find_text(c, XML_SNAME))
            _make_param_info_parse_sub_container(c)
        elif c.tag == "ECUC-PARAM-CONF-CONTAINER-DEF":
            _make_param_info_parse_parameter(c)
        else:
            _make_param_info_parse_sub_container(c)


def _make_param_info_parse_parameter(c_elem):
    # パラメータ
    for c_p in c_elem.findall("PARAMETERS"):
        for c_g in list(c_p):
            if c_g.tag not in _State.result:
                _State.result[c_g.tag] = []
            _State.result[c_g.tag].append(_find_text(c_g, "SHORT-NAME"))

    # 参照
    for c_ref in c_elem.findall("REFERENCES/ECUC-REFERENCE-DEF"):
        _State.result.setdefault("ECUC-REFERENCE-DEF", []).append(
            _find_text(c_ref, "SHORT-NAME"))

    # 外部参照
    for c_ref in c_elem.findall("REFERENCES/ECUC-FOREIGN-REFERENCE-DEF"):
        _State.result.setdefault("ECUC-FOREIGN-REFERENCE-DEF", []).append(
            _find_text(c_ref, "SHORT-NAME"))
        _State.foreign_ref_type[_find_text(c_ref, "SHORT-NAME")] = \
            _find_text(c_ref, "DESTINATION-TYPE")

    # 選択参照
    for c_ref in c_elem.findall("REFERENCES/ECUC-CHOICE-REFERENCE-DEF"):
        _State.result.setdefault("ECUC-CHOICE-REFERENCE-DEF", []).append(
            _find_text(c_ref, "SHORT-NAME"))

    # シンボル参照
    for c_ref in c_elem.findall(
            "REFERENCES/ECUC-SYMBOLIC-NAME-REFERENCE-DEF"):
        _State.result.setdefault(
            "ECUC-SYMBOLIC-NAME-REFERENCE-DEF", []).append(
            _find_text(c_ref, "SHORT-NAME"))

    # インスタンス参照
    for c_ref in c_elem.findall("REFERENCES/ECUC-INSTANCE-REFERENCE-DEF"):
        _State.result.setdefault("ECUC-INSTANCE-REFERENCE-DEF", []).append(
            _find_text(c_ref, "SHORT-NAME"))

    # サブコンテナ (再帰呼出し)
    for c_sub in c_elem.findall("SUB-CONTAINERS"):
        _make_param_info_parse_sub_container(c_sub)


