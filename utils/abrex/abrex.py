def _build_argparser():
    parser = argparse.ArgumentParser(
        prog="abrex.py",
        usage="abrex.py [options]... [yaml|xml files]...",
        description="ABREX: AUTOSAR BSW and RTE XML Generator",
    )
    parser.add_argument("-i", dest="xml_to_yaml", metavar="XML_FILE",
                        help="ARXML to YAML conversion")
    parser.add_argument("-p", dest="param_info", metavar="XML_FILE",
                        help="Generate 'param_info.yaml' from "
                             "AUTOSAR Ecu Configuration Parameters file")
    parser.add_argument("-c", dest="make_csv", metavar="XML_FILE",
                        help="Generate '{BSW_NAME}.csv' from AUTOSAR "
                             "Ecu Configuration Parameters file")
    parser.add_argument("-b", dest="bsw_name", metavar="BSW_NAME",
                        help="set a BSW Module Name "
                             "(for '-c' additional option)")
    parser.add_argument("-e", dest="ecu_extract_ref",
                        metavar="ECU-EXTRACT-REF",
                        help="set a ECU-EXTRACT-REF path if "
                             "<ECUC-VALUE-COLLECTION> is needed")
    parser.add_argument("-v", "--version", action="version",
                        version=VERSION)
    parser.add_argument("-V", "--verbose", action="store_true",
                        help="Show the warning on as conversion is running.")
    parser.add_argument("files", nargs="*",
                        help="YAML or XML files (used for default "
                             "YAML-to-XML conversion)")
    return parser


def main():
    parser = _build_argparser()
    args = parser.parse_args()

    # モード判定 (Ruby 版の挙動に合わせる)
    if args.xml_to_yaml is not None:
        xml_to_yaml(args.xml_to_yaml, args.files)
    elif args.param_info is not None:
        make_param_info(args.param_info)
    elif args.make_csv is not None:
        if args.bsw_name is None:
            parser.print_help()
            sys.exit(1)
        make_csv(args.make_csv, args.bsw_name)
    else:
        if not args.files:
            parser.print_help()
            sys.exit(1)
        yaml_to_xml(args.files, args.ecu_extract_ref, args.verbose)


if __name__ == "__main__":
    main()
