def make_csv(s_file_name, s_target_module):
    if not os.path.isfile(s_file_name):
        sys.exit("Argument error !! [{0}]".format(s_file_name))

    s_csv_file = os.path.join(
        os.path.dirname(s_file_name) or ".", s_target_module + ".csv")

    root = _parse_xml(s_file_name)

    _State.result = OrderedDict()
    _State.container_path = ""

    for c_mod in root.findall(
            "AR-PACKAGES/AR-PACKAGE/AR-PACKAGES/AR-PACKAGE/ELEMENTS/"
            "ECUC-MODULE-DEF"):
        if _find_text(c_mod, "SHORT-NAME") != s_target_module:
            continue
        for c_cont in c_mod.findall(
                "CONTAINERS/ECUC-PARAM-CONF-CONTAINER-DEF"):
            _State.container_path = _find_text(c_cont, "SHORT-NAME")
            _State.result[_State.container_path] = \
                _make_csv_add_parameter_info(c_cont, None)
            _make_csv_parse_parameter(c_cont)

    s_module_path = "/AUTOSAR/EcucDefs/{0}".format(s_target_module)
    s_csv_data = "{0},,,1\n".format(s_module_path)

    for s_param, a_info in _State.result.items():
        if a_info[0] not in _DATA_TYPE_TABLE:
            sys.exit("[{0}] {1}: {2}".format(__file__, "?", a_info[0]))
        if a_info[1] == "1" and a_info[2] == "1":
            s_csv_data += "{0}/{1},{2},{3},1\n".format(
                s_module_path, s_param, s_param.split("/")[-1],
                _DATA_TYPE_TABLE[a_info[0]])
        else:
            s_csv_data += "{0}/{1},{2},{3},{4},{5}\n".format(
                s_module_path, s_param, s_param.split("/")[-1],
                _DATA_TYPE_TABLE[a_info[0]], a_info[1], a_info[2])

    with open(s_csv_file, "w", encoding="utf-8", newline="\n") as fp:
        fp.write(s_csv_data)

    print("Generated {0}".format(s_csv_file))


def _make_csv_add_parameter_info(c_elem, s_name):
    if s_name is None:
        s_name = c_elem.tag

    s_lower = _find_text(c_elem, "LOWER-MULTIPLICITY")
    if s_lower is None:
        sys.exit("[{0}] LOWER-MULTIPLICITY missing".format(__file__))

    s_upper = None
    upper_text = _find_text(c_elem, "UPPER-MULTIPLICITY")
    if upper_text is not None:
        s_upper = upper_text
    else:
        upper_inf = _find_text(c_elem, "UPPER-MULTIPLICITY-INFINITE")
        if upper_inf == "true":
            s_upper = "*"
        else:
            sys.exit("[{0}] UPPER-MULTIPLICITY missing".format(__file__))

    return [s_name, s_lower, s_upper]


def _make_csv_parse_sub_container(c_elem):
    for c in list(c_elem):
        if c.tag == "ECUC-CHOICE-CONTAINER-DEF":
            _State.container_path = "{0}/{1}".format(
                _State.container_path, _find_text(c, "SHORT-NAME"))
            _State.result[_State.container_path] = \
                _make_csv_add_parameter_info(c, None)
            _make_csv_parse_sub_container(c)
            parts = _State.container_path.split("/")
            parts.pop()
            _State.container_path = "/".join(parts)
        elif c.tag == "ECUC-PARAM-CONF-CONTAINER-DEF":
            _State.container_path = "{0}/{1}".format(
                _State.container_path, _find_text(c, "SHORT-NAME"))
            _State.result[_State.container_path] = \
                _make_csv_add_parameter_info(c, None)
            _make_csv_parse_parameter(c)
            parts = _State.container_path.split("/")
            parts.pop()
            _State.container_path = "/".join(parts)
        else:
            _make_csv_parse_sub_container(c)


def _make_csv_parse_parameter(c_elem):
    for c_p in c_elem.findall("PARAMETERS"):
        for c_g in list(c_p):
            s_param = "{0}/{1}".format(
                _State.container_path, _find_text(c_g, "SHORT-NAME"))
            _State.result[s_param] = _make_csv_add_parameter_info(c_g, c_g.tag)

    for tag in ("ECUC-REFERENCE-DEF", "ECUC-FOREIGN-REFERENCE-DEF",
                "ECUC-CHOICE-REFERENCE-DEF",
                "ECUC-SYMBOLIC-NAME-REFERENCE-DEF",
                "ECUC-INSTANCE-REFERENCE-DEF"):
        for c_ref in c_elem.findall("REFERENCES/{0}".format(tag)):
            s_param = "{0}/{1}".format(
                _State.container_path, _find_text(c_ref, "SHORT-NAME"))
            _State.result[s_param] = _make_csv_add_parameter_info(
                c_ref, c_ref.tag)

    for c_sub in c_elem.findall("SUB-CONTAINERS"):
        _make_csv_parse_sub_container(c_sub)


