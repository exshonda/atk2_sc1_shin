def yaml_to_xml(yaml_files, ecu_extract_ref, verbose):
    # 各パラメータのデータ型定義 (-pオプションで生成したものを使用する)
    s_param_file = os.path.join(TOOL_ROOT, "param_info.yaml")
    if not os.path.isfile(s_param_file):
        sys.exit("{0} not found !!".format(s_param_file))
    param_info = _load_yaml(s_param_file)
    if not isinstance(param_info, dict):
        sys.exit("not YAML file !! [{0}]".format(s_param_file))

    # 外部参照先のデータ型格納辞書を分離
    _State.foreign_ref_type = param_info.pop("FOREIGN_REF_TYPE", None) or \
        param_info.pop(":FOREIGN_REF_TYPE", None)

    # 選択型のコンテナ格納配列を分離
    _State.choice_container = param_info.pop("ECUC_CHOICE_CONTAINER_DEF",
                                              None) or \
        param_info.pop(":ECUC_CHOICE_CONTAINER_DEF", None) or []

    # インスタンス参照先のデータ型を保持
    _State.instance_ref_type = param_info.get("ECUC-INSTANCE-REFERENCE-DEF",
                                               []) or []

    # コンテナ名変換テーブル
    _State.ecuc = {}
    _State.dest = {}
    for s_type, a_param in param_info.items():
        if a_param is None:
            continue
        for s_name in a_param:
            if s_type in XML_VALUE_TYPE:
                _State.ecuc[s_name] = XML_VALUE_TYPE[s_type]
            else:
                _State.ecuc[s_name] = "ECUC-NUMERICAL-PARAM-VALUE"
            _State.dest[s_name] = s_type

    # 参照型のパラメータ一覧
    _State.reference_param = (
        (param_info.get("ECUC-REFERENCE-DEF") or []) +
        (param_info.get("ECUC-FOREIGN-REFERENCE-DEF") or []) +
        (param_info.get("ECUC-SYMBOLIC-NAME-REFERENCE-DEF") or []) +
        (param_info.get("ECUC-INSTANCE-REFERENCE-DEF") or [])
    )

    # 与えられた全 YAML をマージする
    h_yaml = OrderedDict()
    s_arxml_name = None
    for s_file_name in yaml_files:
        if not os.path.isfile(s_file_name):
            sys.exit("Argument error !! [{0}]".format(s_file_name))

        # 出力ファイル名作成 (複数ある場合，最初のファイル名を採用する)
        if os.path.splitext(s_file_name)[1] == ".yaml":
            if s_arxml_name is None:
                base = os.path.splitext(os.path.basename(s_file_name))[0]
                s_arxml_name = os.path.join(
                    os.path.dirname(s_file_name) or ".", base + ".arxml")
        else:
            sys.exit("not YAML file name !! [{0}]".format(s_file_name))

        h_tmp = _load_yaml(s_file_name)
        if not isinstance(h_tmp, dict):
            sys.exit("not YAML file !! [{0}]".format(s_file_name))

        # 読み込んだデータをマージしていく
        _yaml_to_xml_merge_hash(h_yaml, h_tmp)

    # XML ツリー組み立て
    xml_autosar = ET.Element("AUTOSAR", attrib=dict(XML_AUTOSAR_FIXED_ATT))
    xml_ar_packages = ET.SubElement(xml_autosar, "AR-PACKAGES")

    # IMPLEMENTATION-DATA-TYPE 格納辞書を分離して先に登録
    h_impl_data_type = h_yaml.pop("IMPLEMENTATION-DATA-TYPE", None)
    if h_impl_data_type is not None:
        xml_ar_package = ET.SubElement(xml_ar_packages, "AR-PACKAGE")
        _add_subelem(xml_ar_package, XML_SNAME, text="ImplementationDataTypes")
        xml_elements = ET.SubElement(xml_ar_package, "ELEMENTS")
        for s_short_name, h_data in h_impl_data_type.items():
            xml_idt = ET.SubElement(xml_elements, "IMPLEMENTATION-DATA-TYPE")
            _add_subelem(xml_idt, XML_SNAME, text=s_short_name)
            _add_subelem(xml_idt, "CATEGORY", text=h_data["CATEGORY"])

    a_module_paths = []
    for s_package_name, h_package_data in h_yaml.items():
        xml_ar_package = ET.SubElement(xml_ar_packages, "AR-PACKAGE")
        _add_subelem(xml_ar_package, XML_SNAME, text=s_package_name)
        xml_elements = ET.SubElement(xml_ar_package, "ELEMENTS")

        for s_module_name, h_module_data in h_package_data.items():
            a_module_paths.append("/{0}/{1}".format(s_package_name,
                                                     s_module_name))
            xml_module = ET.SubElement(xml_elements,
                                        "ECUC-MODULE-CONFIGURATION-VALUES")
            _add_subelem(xml_module, XML_SNAME, text=s_module_name)
            if "DefinitionRef" in h_module_data:
                s_module_def_ref = h_module_data["DefinitionRef"]
                del h_module_data["DefinitionRef"]
            else:
                if verbose:
                    print("This {0} is not defined for 'DefinitionRef'."
                          .format(s_module_name))
                s_module_def_ref = s_module_name
            _add_subelem(xml_module, "DEFINITION-REF",
                          attrib={"DEST": "ECUC-MODULE-DEF"},
                          text=XML_ROOT_PATH + s_module_def_ref)
            _add_subelem(xml_module, "ECUC-DEF-EDITION", text=XML_EDITION)
            _add_subelem(xml_module, "IMPLEMENTATION-CONFIG-VARIANT",
                          text="VARIANT-PRE-COMPILE")
            xml_containers = ET.SubElement(xml_module, "CONTAINERS")

            # 各パラメータ用コンテナを作成する
            for s_short_name, h_pi in h_module_data.items():
                if "DefinitionRef" not in h_pi:
                    h_pi["DefinitionRef"] = s_short_name
                _yaml_to_xml_make_container(
                    xml_containers, s_short_name, h_pi,
                    XML_ROOT_PATH + s_module_def_ref)

    # ECU-EXTRACT-REF 指定がある場合，<ECUC-VALUE-COLLECTION> を追加する
    if ecu_extract_ref is not None:
        xml_ar_package = ET.SubElement(xml_ar_packages, "AR-PACKAGE")
        _add_subelem(xml_ar_package, XML_SNAME, text="EcucValueCollection")
        xml_elements = ET.SubElement(xml_ar_package, "ELEMENTS")
        xml_evc = ET.SubElement(xml_elements, "ECUC-VALUE-COLLECTION")
        _add_subelem(xml_evc, XML_SNAME, text="EcucValueCollection")
        _add_subelem(xml_evc, "ECU-EXTRACT-REF",
                      attrib={"DEST": "SYSTEM"}, text=ecu_extract_ref)
        xml_values = ET.SubElement(xml_evc, "ECUC-VALUES")
        for s_path in a_module_paths:
            xml_cond = ET.SubElement(
                xml_values,
                "ECUC-MODULE-CONFIGURATION-VALUES-REF-CONDITIONAL")
            _add_subelem(xml_cond,
                          "ECUC-MODULE-CONFIGURATION-VALUES-REF",
                          attrib={
                              "DEST": "ECUC-MODULE-CONFIGURATION-VALUES"},
                          text=s_path)

    # XML 文字列生成
    s_xml = _serialize_xml(xml_autosar, version_comment=VER_INFO)

    # ファイル出力
    with open(s_arxml_name, "w", encoding="utf-8", newline="\n") as fp:
        fp.write(s_xml)

    print("Generated {0}".format(s_arxml_name))


def _yaml_to_xml_merge_hash(h_base, h_add):
    for s_key, x_val in h_add.items():
        if s_key not in h_base:
            h_base[s_key] = x_val
        elif h_base[s_key] != x_val:
            if isinstance(h_base[s_key], dict) and isinstance(x_val, dict):
                _yaml_to_xml_merge_hash(h_base[s_key], x_val)
            elif isinstance(h_base[s_key], list):
                if isinstance(x_val, list):
                    h_base[s_key].extend(x_val)
                else:
                    h_base[s_key].append(x_val)
            elif isinstance(x_val, list):
                h_base[s_key] = [h_base[s_key]] + list(x_val)
            else:
                h_base[s_key] = [h_base[s_key], x_val]
        # 同値ならなにもしない


def _yaml_to_xml_make_container(parent, s_short_name, h_param_info, s_path):
    # "ECUC-CONTAINER-VALUE" 要素を作成
    cContainer = ET.SubElement(parent, "ECUC-CONTAINER-VALUE")
    _add_subelem(cContainer, XML_SNAME, text=s_short_name)

    def_ref = h_param_info["DefinitionRef"]
    if def_ref in (_State.choice_container or []):
        _add_subelem(cContainer, "DEFINITION-REF",
                      attrib={"DEST": "ECUC-CHOICE-CONTAINER-DEF"},
                      text="{0}/{1}".format(s_path, def_ref))
    else:
        _add_subelem(cContainer, "DEFINITION-REF",
                      attrib={"DEST": "ECUC-PARAM-CONF-CONTAINER-DEF"},
                      text="{0}/{1}".format(s_path, def_ref))

    # 各パラメータ設定 (パラメータが無い場合は不要)
    if len(h_param_info) == 1:
        return cContainer

    h_check = {XML_PARAM: None, XML_REFER: None, XML_SUB: None}

    # まず参照型，サブコンテナ以外を生成 (AUTOSAR スキーマ制約)
    for s_param_name, sah_value in h_param_info.items():
        if s_param_name == "DefinitionRef" or isinstance(sah_value, dict):
            continue

        if s_param_name in (_State.reference_param or []):
            continue

        # パラメータ名チェック
        if (s_param_name not in (_State.ecuc or {}) or
                s_param_name not in (_State.dest or {})):
            sys.exit("Unknown parameter: {0}".format(s_param_name))

        if h_check[XML_PARAM] is None:
            h_check[XML_PARAM] = ET.SubElement(cContainer, XML_PARAM)

        # 多重度 * 対応
        a_temp = sah_value if isinstance(sah_value, list) else [sah_value]
        for s_val in a_temp:
            ec_type = _State.ecuc[s_param_name]
            xml_p = ET.SubElement(h_check[XML_PARAM], ec_type)
            _add_subelem(xml_p, "DEFINITION-REF",
                          attrib={"DEST": _State.dest[s_param_name]},
                          text="{0}/{1}/{2}".format(
                              s_path, def_ref, s_param_name))
            _add_subelem(xml_p, "VALUE", text=str(s_val))

    # 次に参照型を生成 (AUTOSAR スキーマ制約)
    for s_param_name, sah_value in h_param_info.items():
        if s_param_name == "DefinitionRef" or isinstance(sah_value, dict):
            continue

        if s_param_name in (_State.instance_ref_type or []):
            if not isinstance(sah_value, list):
                sys.exit("{0} must be Array !!".format(s_param_name))
            if s_param_name not in XML_INSTANCE_REF_CONTAINER:
                sys.exit("{0} is not supported !!".format(s_param_name))

            if h_check[XML_REFER] is None:
                h_check[XML_REFER] = ET.SubElement(cContainer, XML_REFER)

            # 多重度 * 対応 (2 次元配列かチェック)
            if (sah_value and isinstance(sah_value[0], list)):
                a_temp = sah_value
            else:
                a_temp = [sah_value]
            for a_val in a_temp:
                ec_type = _State.ecuc[s_param_name]
                xml_p = ET.SubElement(h_check[XML_REFER], ec_type)
                _add_subelem(xml_p, "DEFINITION-REF",
                              attrib={"DEST": _State.dest[s_param_name]},
                              text="{0}/{1}/{2}".format(
                                  s_path, def_ref, s_param_name))
                xml_iref = ET.SubElement(xml_p, "VALUE-IREF")
                for h_val in a_val:
                    for s_p, s_d in XML_INSTANCE_REF_CONTAINER[
                            s_param_name].items():
                        if s_p in h_val:
                            _add_subelem(xml_iref, s_p,
                                          attrib={"DEST": s_d},
                                          text=str(h_val[s_p]))

        elif s_param_name in (_State.reference_param or []):
            if h_check[XML_REFER] is None:
                h_check[XML_REFER] = ET.SubElement(cContainer, XML_REFER)

            a_temp = sah_value if isinstance(sah_value, list) else [sah_value]
            for s_val in a_temp:
                ec_type = _State.ecuc[s_param_name]
                xml_p = ET.SubElement(h_check[XML_REFER], ec_type)
                _add_subelem(xml_p, "DEFINITION-REF",
                              attrib={"DEST": _State.dest[s_param_name]},
                              text="{0}/{1}/{2}".format(
                                  s_path, def_ref, s_param_name))
                if (_State.foreign_ref_type and
                        s_param_name in _State.foreign_ref_type):
                    _add_subelem(xml_p, "VALUE-REF",
                                  attrib={
                                      "DEST": _State.foreign_ref_type[
                                          s_param_name]},
                                  text=str(s_val))
                else:
                    _add_subelem(xml_p, "VALUE-REF",
                                  attrib={"DEST": "ECUC-CONTAINER-VALUE"},
                                  text=str(s_val))

    # 最後にサブコンテナを生成
    for s_param_name, sah_value in h_param_info.items():
        if s_param_name == "DefinitionRef" or not isinstance(sah_value, dict):
            continue
        if h_check[XML_SUB] is None:
            h_check[XML_SUB] = ET.SubElement(cContainer, XML_SUB)
        if "DefinitionRef" not in sah_value:
            sah_value["DefinitionRef"] = s_param_name
        _yaml_to_xml_make_container(
            h_check[XML_SUB], s_param_name, sah_value,
            "{0}/{1}".format(s_path, def_ref))

    return cContainer


