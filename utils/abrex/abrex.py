def _strip_ns(elem):
    """要素ツリーからネームスペースを除去 (REXML 互換動作のため)."""
    for el in elem.iter():
        tag = el.tag
        if isinstance(tag, str) and tag.startswith("{"):
            el.tag = tag.split("}", 1)[1]
        # 属性のネームスペースも除去
        new_attrib = {}
        for k, v in list(el.attrib.items()):
            if k.startswith("{"):
                new_attrib[k.split("}", 1)[1]] = v
            else:
                new_attrib[k] = v
        el.attrib = new_attrib


def _parse_xml(file_path):
    tree = ET.parse(file_path)
    root = tree.getroot()
    _strip_ns(root)
    return root


def _add_subelem(parent, tag, attrib=None, text=None):
    if attrib is None:
        attrib = {}
    el = ET.SubElement(parent, tag, attrib=attrib)
    if text is not None:
        el.text = text
    return el


def _find_text(elem, tag):
    sub = elem.find(tag)
    if sub is None:
        return None
    return sub.text


def _find_all(elem, path):
    return elem.findall(path)


def _serialize_xml(root, version_comment=None):
    """ET.Element を文字列にシリアライズし，Ruby 版の最終整形と同等の
    後処理を施す．"""
    # ET には XML 宣言と pretty-print, comment 注入機能がないため自作する．
    raw = _xml_to_string_pretty(root, indent="  ")
    out = '<?xml version="1.0" encoding="UTF-8"?>\n'
    if version_comment is not None:
        out += '<!--{0}-->\n'.format(version_comment)
    out += raw
    # 値だけが入る要素 (中身が単純テキスト) は <tag>VAL</tag> に折りたたむ
    out = re.sub(
        r">\n[\s]+([\w\.\[\]\(\)\+\-/\*~&;\s]*?)\n[\s]+<",
        r">\1<",
        out,
    )
    # インデントをタブにする
    out = out.replace("  ", "\t")
    return out


def _xml_to_string_pretty(elem, indent="  ", level=0):
    """XML 要素を文字列に整形する (簡易 pretty-print).

    属性値はダブルクォートで，子要素や text を綺麗に整形する．
    """
    pad = indent * level
    # 開きタグの組み立て
    attr_str = ""
    for k, v in elem.attrib.items():
        # 属性値中の特殊文字をエスケープ
        v_esc = (v.replace("&", "&amp;")
                  .replace("<", "&lt;")
                  .replace('"', "&quot;"))
        attr_str += ' {0}="{1}"'.format(k, v_esc)

    children = list(elem)
    text = (elem.text or "")
    text_stripped = text.strip()

    if not children and not text_stripped:
        return "{0}<{1}{2}/>\n".format(pad, elem.tag, attr_str)

    if not children:
        # テキストのみ
        text_esc = (text_stripped.replace("&", "&amp;")
                                  .replace("<", "&lt;"))
        return "{0}<{1}{2}>{3}</{1}>\n".format(
            pad, elem.tag, attr_str, text_esc)

    # 子要素あり
    out = "{0}<{1}{2}>\n".format(pad, elem.tag, attr_str)
    for c in children:
        out += _xml_to_string_pretty(c, indent, level + 1)
    out += "{0}</{1}>\n".format(pad, elem.tag)
    return out


