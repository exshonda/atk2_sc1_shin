def xml_to_yaml(s_first_file, a_extra_file):
    files = [s_first_file] + list(a_extra_file)
    for s_file_name in files:
        if not os.path.isfile(s_file_name):
            sys.exit("Argument error !! [{0}]".format(s_file_name))
        if os.path.splitext(s_file_name)[1] != ".arxml":
            sys.exit("not ARXML file !! [{0}]".format(s_file_name))

        base = os.path.splitext(os.path.basename(s_file_name))[0]
        s_yaml_name = os.path.join(
            os.path.dirname(s_file_name) or ".", base + ".yaml")

        root = _parse_xml(s_file_name)
        h_result = OrderedDict()

        # /AUTOSAR/AR-PACKAGES/AR-PACKAGE 直下の各 ECUC-MODULE-...
        for c_pkg in root.findall("AR-PACKAGES/AR-PACKAGE"):
            for c_mod in c_pkg.findall(
                    "ELEMENTS/ECUC-MODULE-CONFIGURATION-VALUES"):
                s_pkg = _find_text(c_pkg, "SHORT-NAME")
                if s_pkg not in h_result:
                    h_result[s_pkg] = OrderedDict()

                s_mod = _find_text(c_mod, "SHORT-NAME")
                h_result[s_pkg][s_mod] = OrderedDict()

                for c_cont in c_mod.findall(
                        "CONTAINERS/ECUC-CONTAINER-VALUE"):
                    _xml_to_yaml_parse_parameter(
                        c_cont, h_result[s_pkg][s_mod])

        _dump_yaml(h_result, s_yaml_name)
        print("Generated {0}".format(s_yaml_name))


def _xml_to_yaml_parse_parameter(c_elem, h_target):
    s_short = _find_text(c_elem, "SHORT-NAME")
    s_def_ref = _find_text(c_elem, "DEFINITION-REF") or ""
    s_def_name = s_def_ref.split("/")[-1]
    h_target[s_short] = OrderedDict()
    if s_short != s_def_name:
        h_target[s_short]["DefinitionRef"] = s_def_name

    # パラメータ
    for c_pv in c_elem.findall("PARAMETER-VALUES"):
        for tag in ("ECUC-NUMERICAL-PARAM-VALUE",
                    "ECUC-TEXTUAL-PARAM-VALUE"):
            for c_g in c_pv.findall(tag):
                s_name = (_find_text(c_g, "DEFINITION-REF") or
                          "").split("/")[-1]
                s_val = _find_text(c_g, "VALUE")
                _xml_to_yaml_set_value(h_target[s_short], s_name, s_val)

    # 参照，外部参照，選択参照
    for c_ref in c_elem.findall(
            "REFERENCE-VALUES/ECUC-REFERENCE-VALUE"):
        s_name = (_find_text(c_ref, "DEFINITION-REF") or
                  "").split("/")[-1]
        v_ref_text = _find_text(c_ref, "VALUE-REF")
        if v_ref_text is None:
            sys.exit("<VALUE> is not found in '{0}'".format(s_short))
        _xml_to_yaml_set_value(h_target[s_short], s_name, v_ref_text)

    # インスタンス参照
    for c_iref in c_elem.findall(
            "REFERENCE-VALUES/ECUC-INSTANCE-REFERENCE-VALUE"):
        s_name = (_find_text(c_iref, "DEFINITION-REF") or
                  "").split("/")[-1]
        h_target[s_short][s_name] = []
        c_value_iref = c_iref.find("VALUE-IREF")
        if c_value_iref is not None:
            ctx = _find_text(c_value_iref, "CONTEXT-ELEMENT-REF")
            tgt = _find_text(c_value_iref, "TARGET-REF")
            h_target[s_short][s_name].append(
                OrderedDict([("CONTEXT-ELEMENT-REF", ctx)]))
            h_target[s_short][s_name].append(
                OrderedDict([("TARGET-REF", tgt)]))

    # サブコンテナ (再帰呼出し)
    for c_sub in c_elem.findall("SUB-CONTAINERS/ECUC-CONTAINER-VALUE"):
        _xml_to_yaml_parse_parameter(c_sub, h_target[s_short])


def _xml_to_yaml_set_value(target, s_name, s_value):
    """同じパラメータ名が複数回出現したら配列化する."""
    if s_name in target:
        cur = target[s_name]
        if isinstance(cur, list):
            cur.append(s_value)
        else:
            target[s_name] = [cur, s_value]
    else:
        target[s_name] = s_value


